# Range from 0 to 7
for i in range(8):
    print(i)

print("\n")

# Range from 3 to 8
for i in range(3, 8):
    print(i)

print("\n")

# Range from 1 to 50, step 4
for i in range(1, 51, 4):
    print(i)

print("\n")

# Range from 7 to 70, step 7
for i in range(7, 71, 7):
    print(i)



'''
Range functions returns a sequenc of numbers, starting from 0 by default, 
and increments by 1(by default), and stops before a specified number.

syntax of range function:-
range(start?, stop, step?)
'''