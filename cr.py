numbers = (1, 2, 3, 3, 4, 5)

# Find the index of an element at first occurrence
index_of_3 = numbers.index(3)
print(index_of_3)  # Output: 2

# Count total occurrences of an element
count_of_3 = numbers.count(3)
print(count_of_3)  # Output: 2



