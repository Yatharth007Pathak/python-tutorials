num_int = 5
num_float = 2.0
result = num_int + num_float             # num_int is implicitly converted to float
print(result)                            # Output: 7.0



'''
Implicit type conversion is automatically performed by the compiler or interpreter without the programmer's intervention. 
It typically occurs when operations involve mixed data types, and the language converts one or more operands 
to a common type that can handle the operation.
'''