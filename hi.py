# OOPS:- Object-Oriented Programming System

'''
To map with real world scenarios, we started using objects in code.
This is called object oriented programming
Object-Oriented Programming (OOP) in Python is a paradigm that uses objects and classes 
to structure software in a way that models real-world entities.

The key concepts and components of OOP in Python:

1. Classes and Objects
Class: A blueprint for creating objects. It defines a set of attributes and methods that the created objects can have.
Object: An instance of a class. It is a real-world entity with attributes (data) and methods (functions).

2. Encapsulation
Encapsulation is the mechanism of hiding the internal state of an object and only exposing a controlled interface. 
In Python, this is typically done using private attributes and methods.

3. Inheritance
Inheritance allows a class to inherit attributes and methods from another class. 
This promotes code reuse and establishes a natural hierarchy.

4. Polymorphism
Polymorphism allows methods to be used interchangeably with different objects, making code more flexible and scalable.

5. Abstraction
Abstraction means hiding complex implementation details and showing only the necessary features of an object.

Summary
Classes and Objects: Define and instantiate objects.
Encapsulation: Hide data and restrict access to methods.
Inheritance: Create a hierarchy by inheriting from base classes.
Polymorphism: Use methods interchangeably with different objects.
Abstraction: Expose only necessary parts of the objects, hiding complexity.
Using these principles, Python allows for creating complex, modular, and reusable code structures.
'''