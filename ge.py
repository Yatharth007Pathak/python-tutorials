cities = ["delhi", "pune", "kanpur", "noida", "mumbai", "chennai"]
movies = ["guide", "anand", "waqt", "pyaasa", "anaari", "karz", "don", "deewar"]

def print_list(list):
    for item in list:
        print(item, end = " ")

print_list(cities)

print("\n")

print_list(movies)



