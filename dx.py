# Loop through a list
eatables = ["apple", "banana", "cherry"]
for fruit in eatables :
    print(fruit)

# Loop through a range of numbers
for i in range(5):
    print(i)



'''
A for loop is used to iterate over a sequence (such as a list, tuple, dictionary, set, or string) or other iterable objects.

Basic Syntax:
for element in sequence:
    # Code block to be executed for each element
'''