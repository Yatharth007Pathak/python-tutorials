student = {"name": "John", "age": 21, "courses": ["Math", "Science"]}

# Iterate through keys
for key in student:
    print(key)  # Output: name, age, courses

# Iterate through values
for value in student.values():
    print(value)  # Output: John, 21, ['Math', 'Science']

# Iterate through key-value pairs
for key, value in student.items():
    print(key, value)  # Output: name John, age 21, courses ['Math', 'Science']



# Iterating Through Dictionaries:- We can iterate through keys, values, or key-value pairs in a dictionary.