# An empty list
empty_list = []
print(empty_list)

# A list of integers
numbers = [1, 2, 3, 4, 5]
print(numbers)
print(type(numbers))

# A list of strings
fruits = ["apple", "banana", "cherry"]
print(fruits)

# A list containing mixed data types
mixed_list = [1, "hello", 3.14, True]
print(mixed_list)

# A nested list
nested_list = [1, [2, 3], [4, 5, 6]]
print(nested_list)



