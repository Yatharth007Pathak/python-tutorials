binary_number = '101'
padded_binary = binary_number.zfill(8)
print(padded_binary)


# The .zfill() method is used to pad a string with leading zeros so that it reaches a specified length.

'''
Usage
Syntax: string.zfill(width)
string is the string you want to pad.
width is the total length you want the string to be after padding
'''

"""
Explanation of our code:-
Input String: '101'
Desired Width: 8
Padding Applied: Leading zeros are added to make the total length of the string 8 characters.
""" 