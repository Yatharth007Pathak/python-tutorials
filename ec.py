# Dictionary
person = {"name": "Alice", "age": 25, "city": "New York"}

# Loop through keys
for key in person:
    print(key)

print("\n")

# Loop through values
for value in person.values():
    print(value)

print("\n")

# Loop through key-value pairs
for key, value in person.items():
    print(key, value)



# Looping Through Dictionaries:- We can iterate through dictionaries to get keys, values, or key-value pairs.