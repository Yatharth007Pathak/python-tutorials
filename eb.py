# Nested for loop
for i in range(3):
    for j in range(2):
        print(f"i = {i}, j = {j}")

print("\n")

# Nested while loop
i = 0
while i < 3:
    j = 0
    while j < 2:
        print(f"i = {i}, j = {j}")
        j += 1
    i += 1




# Nested Loops:- We can also nest loops, meaning we can have a loop inside another loop.