text = r"Hello\nWorld"
print(text)
# Output: Hello\nWorld

path = r"C:\Users\Name\Documents"
print(path)
# Output: C:\Users\Name\Documents


'''
Raw Strings
In some cases, we might want to use a backslash in a string without it being interpreted as an escape sequence.
We can achieve this by prefixing the string with r or R.
'''