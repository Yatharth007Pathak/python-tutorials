# Tuple unpacking
fruits = ("apple", "banana", "cherry")
(a, b, c) = fruits
print(a)  # Output: apple
print(b)  # Output: banana
print(c)  # Output: cherry

# Unpacking with star expression
numbers = (1, 2, 3, 4, 5)
a, *b, c = numbers
print(a)  # Output: 1
print(b)  # Output: [2, 3, 4]
print(c)  # Output: 5



# Tuple Unpacking:- We can unpack tuples into variables.